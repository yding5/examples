import torch



class Quantized_Active(torch.autograd.Function):
    """ Quantized activation operation

        Arguments:
            significant_bit: bit of quantization, default: 2-bit
            loss_regu: loss regularization term coefficient, currently not applied
    """

    def __init__(self, significant_bit=2):
        self.significant_bit = significant_bit
        self.threshold = pow(2, self.significant_bit)-1
        self.lb = 0
        self.rb = pow(2, self.significant_bit)-1
        #print(self.rb)

    def forward(self, input):
        self.save_for_backward(input)
        maxValue = torch.max(input)
        input_mod = torch.round(torch.clamp(input*255/maxValue, self.lb, self.rb))/255*maxValue
        return input_mod

    def backward(self, grad_output):
        input, = self.saved_tensors
        grad_input = grad_output.clone()
        grad_input = grad_input*(input.le(self.lb)^1).float()
        grad_input = grad_input*(input.ge(self.rb)^1).float()
        return grad_input

class Quantized_ActLayer_baseline(torch.nn.Module):
    """ Customized activation layer quantization

        Arguments: 
            significant_bit: bit of activation layer quantization, default is 2
            loss_regu: additional regularization term coefficient
    """
    def __init__(self,significant_bit=2):
        super(Quantized_ActLayer_baseline, self).__init__()
        self.significant_bit = significant_bit

    def forward(self,input):
        return Quantized_Active(significant_bit=self.significant_bit)(input)


class TransformerNet(torch.nn.Module):
    def __init__(self):
        super(TransformerNet, self).__init__()
        # Initial convolution layers
        self.conv1 = ConvLayer(3, 32, kernel_size=9, stride=1)
        self.in1 = torch.nn.InstanceNorm2d(32, affine=True)
        self.conv2 = ConvLayer(32, 64, kernel_size=3, stride=2)
        self.in2 = torch.nn.InstanceNorm2d(64, affine=True)
        self.conv3 = ConvLayer(64, 128, kernel_size=3, stride=2)
        self.in3 = torch.nn.InstanceNorm2d(128, affine=True)
        # Residual layers
        self.res1 = ResidualBlock(128)
        self.res2 = ResidualBlock(128)
        self.res3 = ResidualBlock(128)
        self.res4 = ResidualBlock(128)
        self.res5 = ResidualBlock(128)
        # Upsampling Layers
        self.deconv1 = UpsampleConvLayer(128, 64, kernel_size=3, stride=1, upsample=2)
        self.in4 = torch.nn.InstanceNorm2d(64, affine=True)
        self.deconv2 = UpsampleConvLayer(64, 32, kernel_size=3, stride=1, upsample=2)
        self.in5 = torch.nn.InstanceNorm2d(32, affine=True)
        self.deconv3 = ConvLayer(32, 3, kernel_size=9, stride=1)
        # Non-linearities
        self.relu = torch.nn.ReLU()

    def forward(self, X):
        y = self.relu(self.in1(self.conv1(X)))
        y = self.relu(self.in2(self.conv2(y)))
        y = self.relu(self.in3(self.conv3(y)))
        y = self.res1(y)
        y = self.res2(y)
        y = self.res3(y)
        y = self.res4(y)
        y = self.res5(y)
        y = self.relu(self.in4(self.deconv1(y)))
        y = self.relu(self.in5(self.deconv2(y)))
        y = self.deconv3(y)
        return y


